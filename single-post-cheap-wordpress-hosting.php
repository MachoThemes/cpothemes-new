						<p>On the WordPress.org <a href="https://wordpress.org/about/requirements/" target="_blank">requirements page</a>, it is officially recommended that WordPress sites should be hosted on a minimum PHP version of 7.2. Although WordPress still works as intended with older versions doesn’t mean you shouldn’t upgrade. There are tons of reasons to upgrade to a newer PHP version including increased performance, security and added functionality. </p>
						<p>Having a web host that doesn’t limit you to older versions of software and PHP will ensure that your website will stay up and running and run faster than your competition.</p>
